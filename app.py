import streamlit as st
import pandas as pd

# Беттің баптаулары
st.set_page_config(page_title="Этнохимиялық зертхана", layout="wide")

st.title("🧪 «Этнохимиялық анықтамалық» виртуалды зертханасы")
st.write("Қазақ ұлттық тағамдарының химиялық құрамын зерттеу платформасы")

# Бүйірлік мәзір
menu = st.sidebar.selectbox("Бөлімді таңдаңыз:", 
    ["Басты бет", "Сүт өнімдері", "Ет өнімдері", "Дәнді дақылдар", "Виртуалды калькулятор"])

if menu == "Басты бет":
    st.header("Қош келдіңіз!")
    st.info("Бұл жерде сіз ұлттық тағамдардың молекулалық деңгейдегі құрамын талдай аласыз.")
    st.image("https://images.unsplash.com/photo-1511690656952-34342bb7c2f2?q=80&w=1000") # Мысал сурет

elif menu == "Сүт өнімдері":
    st.header("🥛 Сүт өнімдерінің химиясы")
    food = st.selectbox("Өнімді таңдаңыз:", ["Қымыз", "Шұбат", "Құрт"])
    
    col1, col2 = st.columns(2)
    
    if food == "Қымыз":
        col1.write("**Құрамы:** Сүт қышқылы, этил спирті, аминқышқылдары.")
        col1.latex(r"CH_3CH(OH)COOH") # Сүт қышқылының формуласы
        col2.metric("Витамин С", "20-25 мг/л")
        col2.metric("Ақуыз", "2.1%")

elif menu == "Виртуалды калькулятор":
    st.header("🧮 Энергетикалық құндылықты есептеу")
    st.write("Тағамның мөлшерін енгізіп, оның химиялық энергиясын ($Q$) есептеңіз:")
    
    mass = st.slider("Тағам салмағы (грамм):", 50, 500, 100)
    
    # Қарапайым есептеу формуласы: Q = m * caloric_value
    q_val = mass * 4.1 # Мысалы ретінде алынған коэффицент
    
    st.success(f"Бұл мөлшерден бөлінетін энергия: {q_val} кДж")
    st.latex(f"Q = m \cdot q = {mass} \cdot 4.1 = {q_val} \text{{ кДж}}")
